void ImGuiRenderer::renderSceneTree(const GameObject& curr_node)
{
    ImGui::SetNextItemOpen(true, ImGuiCond_Once);
    if (ImGui::TreeNodeEx(curr_node.m_Name.c_str(),
                          ImGuiTreeNodeFlags_OpenOnArrow)) {
        /*if (ImGui::IsItemActivated()) {
            TM_LOG_TRACE("Clicked");
        }*/
        for (const GameObject* go : curr_node.m_Children) {
            renderSceneTree(*go);
        }
        ImGui::TreePop();
    }
}

void ImGuiRenderer::terminate()
{
    ImGui_ImplOpenGL3_Shutdown();
    ImGui_ImplGlfw_Shutdown();
    ImGui::DestroyContext();
}

void ImGuiRenderer::render()
{
    // feed inputs to dear imgui, start new frame
    ImGui_ImplOpenGL3_NewFrame();
    ImGui_ImplGlfw_NewFrame();
    ImGui::NewFrame();

    if (auto scene = g_Application->getMainScene()) {
        if (auto go = scene->getGameObject()) {
            ImGui::Begin("Scene");
            renderSceneTree(*go);
            ImGui::End();
        }
    }

    ImGui::ShowDemoWindow();

    // Render dear imgui into screen
    ImGui::Render();
    ImGui_ImplOpenGL3_RenderDrawData(ImGui::GetDrawData());

    ImGuiIO& io = ImGui::GetIO();
    if (io.ConfigFlags & ImGuiConfigFlags_ViewportsEnable) {
        GLFWwindow* backup_current_context = glfwGetCurrentContext();
        ImGui::UpdatePlatformWindows();
        ImGui::RenderPlatformWindowsDefault();
        glfwMakeContextCurrent(backup_current_context);
    }

    ImGui::EndFrame();
}

